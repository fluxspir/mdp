#include <stdio.h>

#include "randpass.h"
#include "generate.h"


void
generate_passwords(int length, unsigned int count)
{
	char password[MAX_PASSWORD_LENGTH];

	for (unsigned int i = 0; i < count; i++) {
		generate_password(password, length, "NCL");
		printf("%s\n", password);
	}
}
